internal enum BytewordsConstants {
    static let words = BCUR.bytewords
    static let bytemojis = BCUR.bytemojis

    static let minimals: [String] = words.map { word in
        let first = word.first ?? " "
        let last = word.last ?? " "
        return "\(first)\(last)"
    }

    static let wordIndexes: [String: UInt8] = {
        Dictionary(uniqueKeysWithValues: words.enumerated().map { index, word in
            (word, UInt8(index))
        })
    }()

    static let minimalIndexes: [String: UInt8] = {
        Dictionary(uniqueKeysWithValues: minimals.enumerated().map { index, minimal in
            (minimal, UInt8(index))
        })
    }()

    static let bytemojiSet: Swift.Set<String> = {
        Swift.Set(bytemojis)
    }()

    /// Maps 2-letter first+last abbreviation → full 4-letter word (lowercase).
    static let firstLastToWord: [String: String] = {
        Dictionary(uniqueKeysWithValues: words.map { word in
            let abbr = "\(word.first!)\(word.last!)"
            return (abbr, word)
        })
    }()

    /// Maps first 3 letters → full 4-letter word (lowercase).
    static let firstThreeToWord: [String: String] = {
        Dictionary(uniqueKeysWithValues: words.map { word in
            let prefix = String(word.prefix(3))
            return (prefix, word)
        })
    }()

    /// Maps last 3 letters → full 4-letter word (lowercase).
    static let lastThreeToWord: [String: String] = {
        Dictionary(uniqueKeysWithValues: words.map { word in
            let suffix = String(word.suffix(3))
            return (suffix, word)
        })
    }()
}
