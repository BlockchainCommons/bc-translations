    [Fact]
    public void ChooseDegree200()
    {
        var message = TestHelpers.MakeMessage("Wolf", 1024);
        var fragmentLength = FountainUtils.FragmentLength(message.Length, 100);
        var fragments = FountainUtils.Partition(message, fragmentLength);

        int[] expected =
